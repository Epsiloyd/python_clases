"""
Ejercicio 4: Pedir dos numeors al usuario y hacer todas las operaciones
basicas de una computadora y mostrarlo por pantalla
"""

numero1 = int(input("\nEscribe el primer numero: "))
numero2 = int(input("\nEscribe el segundo numero: "))


print("\n#### CALCULADORA ####")

print(f"\nSuma: {str(numero1 + numero2)} ")

print(f"\nResta: {str(numero1 - numero2)} ")

print(f"\nMultiplicacion: {str(numero1 * numero2)} ")

print(f"\nDivicion: {str(numero1 / numero2)} ")

print("\nFin del Ejercicio")